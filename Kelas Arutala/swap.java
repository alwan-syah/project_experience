public class swap {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;

        int temp = a;
        a = b;
        b = temp;

        System.out.println("Nilai a setelah ditukar: " + a);
        System.out.println("Nilai b setelah ditukar: " + b);
    }

}
